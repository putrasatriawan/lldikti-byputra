        <p>Sejarah perkembangan Kopertis dimulai dengan terbitnya Surat Keputusan Menteri Pendidikan dan Kebudayaan Republik Indonesia Nomor 1/PK/1968 tanggal 17 Februari 1968. Berdasarkan keputusan tersebut dibentuk <b>Koordinator Perguruan Tinggi (KOPERTI)</b> yang mempunyai fungsi sebagai aparatur konsultatif dengan Kepala Kantor Perwakilan Pendidikan dan Kebudayaan setempat. Sehubungan dengan makin bertambahnya pendirian perguruan tinggi terutama Perguruan Tinggi Swasta, maka Menteri Pendidikan dan Kebudayaan mengeluarkan Surat Keputusan Nomor 079/O/1975 tanggal 17 April 1975 yang membatasi ruang lingkup kerja Koordinator Perguruan Tinggi, khususnya untuk memberikan pelayanan kepada Perguruan Tinggi Swasta maka <b>Koordinator Perguruan Tinggi (KOPERTI)</b> di rubah menjadi <b>Koordinator Perguruan Tinggi Swasta (KOPERTIS)</b>.</p>
        <p>Dalam rangka penyesuaian dengan perkembangan di bidang pengelolaan Perguruan Tinggi Swasta, Menteri Pendidikan dan Kebudayaan menerbitkan Surat Keputusan <b>No. 062/O/1982 dan No. 0135/O/1990 tanggal 15 Maret 1990</b>, tentang Organisasi dan Tata Kerja Koordinator Perguruan Tinggi Swasta yang didalamnya selain mengatur susunan organisasi dan tata kerja Kopertis juga merubah Wilayah kerja menjadi 12 Wilayah terdiri dari <b>KOPERTIS Wilayah I</b> Medan, <b>KOPERTIS Wilayah II</b> Palembang, <b>KOPERTIS Wilayah III</b> Jakarta, <b>KOPERTIS Wilayah IV</b> Bandung, <b>KOPERTIS Wilayah v</b> Yogyakarta, <b>KOPERTIS Wilayah VI</b> Semarang, <b>KOPERTIS Wilayah VII</b> Surabaya, <b>KOPERTIS Wilayah VIII</b> Bali, <b>KOPERTIS Wilayah XI</b> Ujung Pandang, <b>KOPERTIS Wilayah X</b> Padang, <b>KOPERTIS Wilayah XI</b> Banjarmasin, dan <b>KOPERTIS Wilayah XII</b> Ambon.</p>
        <p>Dengan semakin berkembangnya Perguruan Tinggi Swasta, Kementerian Pendidikan dan Kebudayaan mengeluarkan Peraturan Menteri Pendidikan dan Kebudayaan Republik Indonesia No. 1 tahun 2013 jo No. 42 tahun 2013. Organisasi dan Tata Kerja Kopertis kembali merubah wilayah kerja menjadi 14 Wilayah dengan bertambahnya Kopertis Wilayah XIII Aceh dan Kopertis Wilayah XIV Papua. Dengan adanya peraturan ini juga beberapa bagian ada yang berubah nama sekaligus merubah uraian tugas bagian tersebut karena sudah tidak sesuai dengan perkembangan pendidikan tinggi sekarang.</p>
        <p>Sesuai peraturan Meteri Riset, Teknologi, dan Pendidikan Tinggi Republik Indonesia nomor 15 tahun 2018 tentang Organisasi dan Tata Kerja Lembaga Layanan Pendidikan Tinggi, maka tahun 2018 Kopertis berubah nama menjadi LLDIKTI yang dipimpin oleh seorang Kepala. LLDIKTI berada di bawah dan bertanggung jawab kepada Menteri Riset, Teknologi, dan Pendidikan Tinggi.</p>
